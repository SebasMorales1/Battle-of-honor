import { Schema, model } from 'mongoose'

const User = new Schema({
  nickname: {
    type: String,
    required: true,
    unique: true,
    trim: true
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true
  },
  password: {
    type: String,
    required: true,
    trim: true
  }
})

export default model('user', User)